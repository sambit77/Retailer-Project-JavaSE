package model;

/**
 *
 * @author Sambit
 */
public class Customers {
    private String name;
    private String phone;
    private String email;
    private String address;
    private String balance;
    private String lastAdded;
    private String lastSubtracted;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getBalance() {
        return balance;
    }

    public void setBalance(String balance) {
        this.balance = balance;
    }

    public String getLastAdded() {
        return lastAdded;
    }

    public void setLastAdded(String lastAdded) {
        this.lastAdded = lastAdded;
    }

    public String getLastSubtracted() {
        return lastSubtracted;
    }

    public void setLastSubtracted(String lastSubtracted) {
        this.lastSubtracted = lastSubtracted;
    }

    
    
}
